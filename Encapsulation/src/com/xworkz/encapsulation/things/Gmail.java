package com.xworkz.encapsulation.things;

public class Gmail {
	private String name;
	private String gender;
	private int age;

	public void setName(String name) {
		this.name = name;

	}

	public String getName() {
		return this.name;
	}

	public void setgender(String gender) {
		this.gender = gender;
	}

	public String getgender() {
		return this.gender;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public int getage() {
		return this.age;
	}
}
